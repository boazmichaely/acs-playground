  acs_curl GET "/v1/groups" | python3 -c '
import json,sys
pid=sys.argv[1]
j=json.load(sys.stdin)
for g in j.get("groups",[]):
    pr=g.get("props",{})
    if pr.get("authProviderId")!=pid: continue
    if pr.get("key","")=="" and pr.get("value","")=="" and g.get("roleName")=="None":
        sys.exit(0)
sys.exit(1)
